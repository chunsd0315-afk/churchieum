function pick<T>(arr: T[], seed: number, salt: number): T {
  return arr[(seed + salt) % arr.length];
}

function buildParts(
  introA: string[],
  introB: string[],
  insights: string[],
  decisions: string[],
  seed: number,
): CopyParts {
  const a = pick(introA, seed, 0);
  const b = pick(introB, seed, 17);
  const intro = `${a}\n${b}`;
  const insight = pick(insights, seed, 31);
  const decision = pick(decisions, seed, 47);
  return { intro, insight, decision };
}

function partsForType(type: GraceNoteType, seed: number): CopyParts {
  if (type === 'reading') {
    return buildParts(READING_INTRO_A, READING_INTRO_B, READING_INSIGHT, READING_DECISION, seed);
  }
  if (type === 'sermon') {
    return buildParts(SERMON_INTRO_A, SERMON_INTRO_B, SERMON_INSIGHT, SERMON_DECISION, seed);
  }
  return buildParts(PERSONAL_INTRO_A, PERSONAL_INTRO_B, PERSONAL_INSIGHT, PERSONAL_DECISION, seed);
}

function titlesForType(type: GraceNoteType): string[] {
  if (type === 'reading') return READING_TITLES_52;
  if (type === 'sermon') return SERMON_TITLES_52;
  return PERSONAL_TITLES_52;
}

export function buildGraceCopyForSeedNote(
  type: GraceNoteType,
  id: string,
  seed: number,
): { graceTitle: string; graceContent: string } {
  const titles = titlesForType(type);
  const titleSalt = seed ^ id.length * 997;
  const graceTitle = titles[(titleSalt + seed * 3) % titles.length];
  const parts = partsForType(type, seed ^ titleSalt);
  const graceContent = `${parts.intro}\n\n${parts.insight}\n\n${parts.decision}`;
  return { graceTitle, graceContent };
}

export function countUniqueTitles(): number {
  return READING_TITLES_52.length + SERMON_TITLES_52.length + PERSONAL_TITLES_52.length;
}

export function countUniqueContentPairs(): number {
  return (
    READING_INTRO_A.length * READING_INTRO_B.length
    + SERMON_INTRO_A.length * SERMON_INTRO_B.length
    + PERSONAL_INTRO_A.length * PERSONAL_INTRO_B.length
  ) * READING_INSIGHT.length;
}
