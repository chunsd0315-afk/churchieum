export const DEFAULT_TITLE_BY_TYPE: Record<GraceNoteType, string> = {
  reading: '말씀을 통해 받은 은혜',
  sermon: '설교를 통해 받은 은혜',
  prayer: '오늘의 기도',
};

export function titlesForType(type: GraceNoteType): string[] {
  if (type === 'reading') return READING_TITLES;
  if (type === 'sermon') return SERMON_TITLES;
  return PERSONAL_TITLES;
}

export function buildGraceContent(introIndex: number, insightIndex: number): string {
  const intro = CONTENT_INTROS[introIndex % CONTENT_INTROS.length];
  const insight = CONTENT_INSIGHTS[insightIndex % CONTENT_INSIGHTS.length];
  return `${intro} ${insight}`;
}

/** content 첫 문장에서 제목 생성 (최대 30자) */
export function titleFromContent(content: string, type: GraceNoteType): string {
  const trimmed = content.trim();
  if (!trimmed) return DEFAULT_TITLE_BY_TYPE[type];
  const firstSentence = trimmed.split(/[.!?。]\s*/)[0]?.trim() ?? trimmed;
  if (firstSentence.length <= 30) return firstSentence;
  return firstSentence.slice(0, 28) + '…';
}
